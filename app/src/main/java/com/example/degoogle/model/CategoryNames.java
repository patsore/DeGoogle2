package com.example.degoogle.model;

public class CategoryNames {

    String name = "";
    int id = 0;

    public CategoryNames(String name, int id) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
